// C++ program to illustrate some of the
// above mentioned functions

#include <iostream>
#include <math.h>

int main()
{
    double x = 2.3;
    std::cout << "Sine value of x=2.3 : " << sin(x)<<"\n";
    std::cout << "Cosine value of x=2.3 : " << cos(x) <<"\n";
    std::cout << "Tangent value of x=2.3 : " << tan(x) << "\n";
}
